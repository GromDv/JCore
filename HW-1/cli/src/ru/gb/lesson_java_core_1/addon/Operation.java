package ru.gb.lesson_java_core_1.addon;

/**
 * Пакет классов операций
 */
public class Operation {
    /**
     * Метод сложения
     * @param a
     * @param b
     * @return
     */
    public static int add(int a, int b) {
        return a + b;
    }
    /**
     * Метод вычитания
     * @param a
     * @param b
     * @return
     */
    public static int sub(int a, int b) {
        return a - b;
    }
    /**
     * Метод умножения
     * @param a
     * @param b
     * @return
     */
    public static int mul(int a, int b) {
        return a * b;
    }
    /**
     * Метод деления
     * @param a
     * @param b
     * @return
     */
    public static int div(int a, int b) {
        return a / b;
    }






}
